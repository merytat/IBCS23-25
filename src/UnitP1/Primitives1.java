package UnitP1;

public class Primitives1 {
    public static void main(String[] args) {
        //create variables
        String stName = "Mery Tellez"; //decl + assignment
        byte stAge;
        long stId;
        char gender;
        double height;
        boolean isIB;

        //assign values
        stAge = 40;
        stId = 175673;
        gender = 'F';
        height = 1.72;
        isIB = true;

        //print values
        System.out.println("Name: " + stName);
        System.out.println("Age: " + stAge);
        System.out.println("Student ID: " + stId);
        System.out.println("Gender: " + gender);
        System.out.println("Height: " + height);
        System.out.println("IB Diploma enrolled: " + isIB);
    }
}
